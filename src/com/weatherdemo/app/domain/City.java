package com.weatherdemo.app.domain;

public class City {
	private int id;
	private int pId;
	private String ciName;
	private String ciCode;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getCiName() {
		return ciName;
	}
	public void setCiName(String ciName) {
		this.ciName = ciName;
	}
	public String getCiCode() {
		return ciCode;
	}
	public void setCiCode(String ciCode) {
		this.ciCode = ciCode;
	}
	
	
}
